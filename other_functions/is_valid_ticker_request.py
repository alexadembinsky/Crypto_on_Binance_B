# Функция проверяет, является ли текст допустимым запросом тикера (может содержать только латинские буквы, * и ?)

def is_valid_ticker_request(text: str) -> bool:
    """
    Проверка, содержит ли текст только допустимые для запроса тикера символы
    (латинские буквы, цифры, * и ?)
    """
    return all(
        ord('A') <= ord(c) <= ord('Z') or  # проверка на заглавные буквы
        ord('a') <= ord(c) <= ord('z') or  # проверка на строчные буквы
        ord('0') <= ord(c) <= ord('9') or  # проверка на цифры
        c in '*?'  # проверка на специальные символы
        for c in text
    )


