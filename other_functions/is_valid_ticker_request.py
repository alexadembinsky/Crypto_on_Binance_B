# Функция проверяет, является ли текст допустимым запросом тикера (может содержать только латинские буквы, * и ?)

def is_valid_ticker_request(text: str) -> bool:
    """
    Проверка, содержит ли текст только допустимые для запроса тикера символы (латинские буквы, * и ?)
    """
    return all(ord('A') <= ord(c) <= ord('Z') or ord('a') <= ord(c) <= ord('z') or c in '*?' for c in text)


